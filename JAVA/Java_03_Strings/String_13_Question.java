// Write a program to format the following letter using escape sequence characters.
    //  Letter = “Dear Harsh, This Java Course is nice. Thanks”

package Java_03_Strings;
public class String_13_Question {
    public static void main(String[] args) {
        System.out.println("-----Before Format-----");
        System.out.println("Dear Harsh, This Java Course is nice. Thanks");
        System.out.println("-----After Format using escape sequence characters-----");
        System.out.println("Dear Harsh,\nThis Java Course is nice.\nThanks");
    }
}
